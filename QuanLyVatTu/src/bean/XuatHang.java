public class XuatHang {
    
    private String idXuat;
    private String idHangHoa;
    private String ngayXuat;
    private int soLuong;
    private String idNhanVien;
    private String idNhap;

    public String getIdXuat() {
        return idXuat;
    }

    public void setIdXuat(String idXuat) {
        this.idXuat = idXuat;
    }

    public String getIdHangHoa() {
        return idHangHoa;
    }

    public void setIdHangHoa(String idHangHoa) {
        this.idHangHoa = idHangHoa;
    }

    public String getNgayXuat() {
        return ngayXuat;
    }

    public void setNgayXuat(String ngayXuat) {
        this.ngayXuat = ngayXuat;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public String getIdNhanVien() {
        return idNhanVien;
    }

    public void setIdNhanVien(String idNhanVien) {
        this.idNhanVien = idNhanVien;
    }

    public String getIdNhap() {
        return idNhap;
    }

    public void setIdNhap(String idNhap) {
        this.idNhap = idNhap;
    }
}
